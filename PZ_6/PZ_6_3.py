# Даны множества A и B, состоящие соответственно из N1 и N2 точек (точки заданы
# своими координатами x, у). Найти минимальное расстояние между точками этих
# множеств и сами точки, расположенные на этом расстоянии (вначале выводится
# точка из множества A, затем точка из множества B).
# Расстояние R между точками с координатами (x1, y 1) и (x 2, у2) вычисляется по формуле:
# R = √(x2 – x1)2 + (у2 – y1)2.
# Для хранения данных о каждом наборе точек следует использовать по два списка: первый
# список для хранения абсцисс, второй — для хранения ординат.
import math

def min_distance(A, B):
    min_dist = float('inf')
    min_point_A = None
    min_point_B = None

    for i in range(len(A)):
        for j in range(len(B)):
            dist = math.sqrt((B[j][0] - A[i][0])**2 + (B[j][1] - A[i][1])**2)
            if dist < min_dist:
                min_dist = dist
                min_point_A = A[i]
                min_point_B = B[j]

    return min_dist, min_point_A, min_point_B

A = [(1, 2), (3, 4), (5, 6)]
B = [(7, 8), (9, 10), (11, 12)]
min_dist, point_A, point_B = min_distance(A, B)
print(f"Минимальное расстояние: {min_dist}")
print(f"Точка из множества A: {point_A}")
print(f"Точка из множества B: {point_B}")